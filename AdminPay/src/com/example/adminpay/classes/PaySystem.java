package com.example.adminpay.classes;

public class PaySystem {
	
	private int id;
	private String pay;
	private String list_val;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getPay() {
		return pay;
	}
	
	public void setPay(String pay) {
		this.pay = pay;
	}
	
	public String getList_val() {
		return list_val;
	}
	
	public void setList_val(String list_val) {
		this.list_val = list_val;
	}	
	
}
